#!/bin/bash

##############################################################################################
echo "=========================Update the yum package manager==================="
sudo yum update -y
echo "============================================================================="
##############################################################################################


#sudo yum-utils device-mapper-persistent-data lvm2
##############################################################################################
echo "=========================Download the docker application from the repo==================="
sudo yum-config-manager --add-repo https://download.docker.com/linux/centos/docker-ce.repo
echo "============================================================================="
##############################################################################################


##############################################################################################
echo "=========================Install the docker application============================"
sudo yum install -y docker-ce
echo "============================================================================="
##############################################################################################
echo "============================================================================="
##############################################################################################


##############################################################################################
echo "=========================Starting the docker application============================"
sudo systemctl start docker
echo "============================================================================="
##############################################################################################


##############################################################################################
echo "=========================Enabling the docker application============================="
sudo systemctl enable docker
echo "============================================================================="
##############################################################################################


##############################################################################################
echo "=========================Getting Docker version============================="
sudo docker --version
echo "============================================================================="
##############################################################################################